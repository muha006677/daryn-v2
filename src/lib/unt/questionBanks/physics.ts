import type { UNTQuestion, DifficultyLevel } from '../types'

function createPhysicsSingle(
  id: string,
  prompt: string,
  options: string[],
  correctAnswer: number,
  explanation: string,
  difficulty: DifficultyLevel,
  topic: string,
  isLogicalTrap: boolean = false
): UNTQuestion {
  return {
    id,
    subject: 'physics',
    type: 'single',
    difficulty,
    prompt,
    options,
    correctAnswers: [correctAnswer],
    explanation,
    topic,
    isLogicalTrap,
  }
}

