function createReadingQuestion(
  id: string,
  passageId: string,
  prompt: string,
  options: string[],
  correctAnswer: number,
  explanation: string,
  difficulty: DifficultyLevel,
  topic: string,
  isLogicalTrap: boolean = false
): UNTQuestion {
  return {
    id,
    subject: 'reading_literacy',
    type: 'single',
    difficulty,
    prompt,
    options,
    correctAnswers: [correctAnswer],
    explanation,
    topic,
    isLogicalTrap,
    passageId,
  }
}

