// 10 сұрақты алу функциясы (сынып бойынша)
export function getLocalQuestions(grade: number = 2): Question[] {
  const normalizedGrade = Math.max(1, Math.min(6, Math.floor(grade)))
  const gradeQuestions = questionsByGrade[normalizedGrade] || questionsByGrade[2]
  
  // Сұрақтарды араластыру
  const shuffled = [...gradeQuestions].sort(() => Math.random() - 0.5)
  
  // Егер 10-нан аз болса, қайталап толтыру
  const result: Question[] = []
  while (result.length < 10) {
    result.push(...shuffled)
  }
  
  return result.slice(0, 10)
}
